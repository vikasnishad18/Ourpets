-- Ourpets admin + editable services catalog.
-- Run this in Supabase SQL Editor (after supabase.sql).

-- Editable services shown on the public site
create table if not exists public.services (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  title text not null,
  description text not null,
  icon text not null default 'paw', -- paw | grooming | medical | training
  is_active boolean not null default true,
  sort_order int not null default 100
);

create index if not exists services_sort_order_idx on public.services (sort_order);

-- Auto-update updated_at on row update
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists trg_services_set_updated_at on public.services;
create trigger trg_services_set_updated_at
before update on public.services
for each row execute procedure public.set_updated_at();

alter table public.services enable row level security;

-- Site photos (admin-managed images used by sections)
create table if not exists public.site_photos (
  id bigint generated by default as identity primary key,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  section text not null, -- hero | products | tips | services | contact | other
  title text,
  url text not null, -- can be relative (e.g. images/p4.png) or absolute
  is_active boolean not null default true,
  sort_order int not null default 100
);

create index if not exists site_photos_section_sort_order_idx on public.site_photos (section, sort_order);

drop trigger if exists trg_site_photos_set_updated_at on public.site_photos;
create trigger trg_site_photos_set_updated_at
before update on public.site_photos
for each row execute procedure public.set_updated_at();

alter table public.site_photos enable row level security;

-- Admin users table (simple allowlist)
create table if not exists public.admin_users (
  user_id uuid primary key references auth.users (id) on delete cascade,
  created_at timestamptz not null default now()
);

alter table public.admin_users enable row level security;

-- Public can read active services
drop policy if exists "public can read active services" on public.services;
create policy "public can read active services"
on public.services
for select
to anon, authenticated
using (is_active = true);

-- Admins can read inquiries (contact/booking submissions)

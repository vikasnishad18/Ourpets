-- Supabase Storage bucket for uploaded images (optional)
do $$
begin
  if to_regclass('storage.buckets') is null or to_regclass('storage.objects') is null then
    raise notice 'storage schema not found; skipping Storage bucket/policies';
    return;
  end if;

  insert into storage.buckets (id, name, public)
  values ('ourpets', 'ourpets', true)
  on conflict (id) do nothing;

  -- Public can read ourpets bucket objects (bucket is public, but policy helps for SQL validation)
  begin
    execute 'drop policy if exists "public can read ourpets bucket" on storage.objects';
    execute $p$
      create policy "public can read ourpets bucket"
      on storage.objects
      for select
      to anon, authenticated
      using (bucket_id = 'ourpets')
    $p$;
  exception when others then
    raise notice 'could not create public storage policy: %', sqlerrm;
  end;

  -- Admins can upload/update/delete in ourpets bucket
  begin
    execute 'drop policy if exists "admins can insert ourpets bucket" on storage.objects';
    execute $p$
      create policy "admins can insert ourpets bucket"
      on storage.objects
      for insert
      to authenticated
      with check (
        bucket_id = 'ourpets'
        and exists (select 1 from public.admin_users au where au.user_id = auth.uid())
      )
    $p$;

    execute 'drop policy if exists "admins can update ourpets bucket" on storage.objects';
    execute $p$
      create policy "admins can update ourpets bucket"
      on storage.objects
      for update
      to authenticated
      using (
        bucket_id = 'ourpets'
        and exists (select 1 from public.admin_users au where au.user_id = auth.uid())
      )
      with check (
        bucket_id = 'ourpets'
        and exists (select 1 from public.admin_users au where au.user_id = auth.uid())
      )
    $p$;

    execute 'drop policy if exists "admins can delete ourpets bucket" on storage.objects';
    execute $p$
      create policy "admins can delete ourpets bucket"
      on storage.objects
      for delete
      to authenticated
      using (
        bucket_id = 'ourpets'
        and exists (select 1 from public.admin_users au where au.user_id = auth.uid())
      )
    $p$;
  exception when others then
    raise notice 'could not create admin storage policies: %', sqlerrm;
  end;
end;
$$;

